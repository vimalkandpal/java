public class pattern19 {
    public static void main(String[]args){
        //this is the first pattern of the same logic question
        int i,j;
        for(i=1;i<=4;i++){
            for(j=1;j<=2*i;j++){
                System.out.print(j);
            }
            System.out.println();
        }
        //this is the second pattern of the same logic question
        int k,l;
        for(k=1;k<=4;k++){
            for(l=1;l<=2*k;l++){
                System.out.print(k);
            }
            System.out.println();
        }
    }
}
